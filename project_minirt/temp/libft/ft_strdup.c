#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*res;

	res = malloc(1 + (sizeof(*res) * ft_strlen(s1)));
	if (res == 0)
		return (0);
	i = 0;
	while (s1[i])
	{
		res[i] = s1[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
